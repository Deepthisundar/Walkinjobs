package com.job.reg;

/**
 *
 * @author mvaikundam
 */
public class Jobseeker {
    private String name;
private String emailId;
private String password;
private int mobile;
private String course;
private String specilization;
private int yearOfPassing;
private int totalExperience;
private String keySkills;
private String currentLocation;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the emailId
     */
    public String getEmailId() {
        return emailId;
    }

    /**
     * @param emailId the emailId to set
     */
    public void setEmailId(String emailId) {
        this.emailId = emailId;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the mobile
     */
    public int getMobile() {
        return mobile;
    }

    /**
     * @param mobile the mobile to set
     */
    public void setMobile(int mobile) {
        this.mobile = mobile;
    }

    /**
     * @return the course
     */
    public String getCourse() {
        return course;
    }

    /**
     * @param course the course to set
     */
    public void setCourse(String course) {
        this.course = course;
    }

    /**
     * @return the specilization
     */
    public String getSpecilization() {
        return specilization;
    }

    /**
     * @param specilization the specilization to set
     */
    public void setSpecilization(String specilization) {
        this.specilization = specilization;
    }

    /**
     * @return the yearOfPassing
     */
    public int getYearOfPassing() {
        return yearOfPassing;
    }

    /**
     * @param yearOfPassing the yearOfPassing to set
     */
    public void setYearOfPassing(int yearOfPassing) {
        this.yearOfPassing = yearOfPassing;
    }

    /**
     * @return the totalExperience
     */
    public int getTotalExperience() {
        return totalExperience;
    }

    /**
     * @param totalExperience the totalExperience to set
     */
    public void setTotalExperience(int totalExperience) {
        this.totalExperience = totalExperience;
    }

    /**
     * @return the keySkills
     */
    public String getKeySkills() {
        return keySkills;
    }

    /**
     * @param keySkills the keySkills to set
     */
    public void setKeySkills(String keySkills) {
        this.keySkills = keySkills;
    }

    /**
     * @return the currentLocation
     */
    public String getCurrentLocation() {
        return currentLocation;
    }

    /**
     * @param currentLocation the currentLocation to set
     */
    public void setCurrentLocation(String currentLocation) {
        this.currentLocation = currentLocation;
    }
    
}
